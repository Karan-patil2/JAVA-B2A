public class ternary {
    public static void main(String[] args) {
        int age = 20;
        String A = age >= 18 ? "age is over 18" : "Still a kid";// if age is greater than 18 than it will print 1st statement orelse 2nd statement.
        System.out.print(A);
    }
}
// ternary operator  : format Op1 ? Op2 : Op3;
//It is like if-else statement , we can either print this or that .